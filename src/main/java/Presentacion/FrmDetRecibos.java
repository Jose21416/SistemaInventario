    private void tblRecibosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRecibosMousePressed
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            String idrecibo = tblRecibos.getValueAt(tblRecibos.getSelectedRow(),0).toString();
            String fecha = tblRecibos.getValueAt(tblRecibos.getSelectedRow(),1).toString();
            String pentrega = tblRecibos.getValueAt(tblRecibos.getSelectedRow(),2).toString();
            String precibe = tblRecibos.getValueAt(tblRecibos.getSelectedRow(),3).toString();

            FrmDetalleRecibos mi = new FrmDetalleRecibos(new JFrame(), true);
            mi.setFactura(idrecibo, fecha, pentrega, precibe);
            mi.setLocationRelativeTo(null);
            mi.setVisible(true);
        }
    }//GEN-LAST:event_tblRecibosMousePressed

