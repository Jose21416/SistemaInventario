    public boolean estaenTabla(String texto) {

        boolean res = false;
        DecimalFormat ft = new DecimalFormat("####.00");
        for (int i = 0; i < tblRecibos.getRowCount(); i++) {

            String codigo = tblRecibos.getValueAt(i, 0).toString();
            if (codigo.equals(texto)) {
                int cantidad = Integer.parseInt(tblRecibos.getValueAt(i, 2).toString());
                double preciounitario = Double.parseDouble(tblRecibos.getValueAt(i, 3).toString());
                int totalcantidad = cantidad + 1;
                tblRecibos.setValueAt(totalcantidad, i, 2);

                double totalparcial = totalcantidad * preciounitario;
                tblRecibos.setValueAt(ft.format(totalparcial), i, 6);
                res = true;
            }

        }
        sumarTotal();
        return res;
    }
    
    public void sumarTotal() {

        DecimalFormat ft = new DecimalFormat("####.00");
        double totalparcial = 0, totalfinal = 0;
        for (int i = 0; i < tblRecibos.getRowCount(); i++) {

            totalparcial = Double.parseDouble(tblRecibos.getValueAt(i, 6).toString());
            totalfinal += totalparcial;
            txtTotal.setText(ft.format(totalfinal));
        }

    }
    
    public void mostrarProductos(String  producto) {

        LAlmacen fn = new LAlmacen();
        DAlmacen dts = new DAlmacen();
        DefaultTableModel tblDatos = (DefaultTableModel) tblRecibos.getModel();
        dts.setIdAlmacen(producto);
        String registros[] = fn.mostrarbuscarProductosRec(dts);
        if (registros[0] != null) {

            tblDatos.insertRow(0, registros);
            tblRecibos.setModel(tblDatos);

        } else {

         JOptionPane.showMessageDialog(rootPane, "Producto no existe en el inventario, favor de validar.");

        }

    }
    
