    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            boolean b = estaenTabla(txtBuscar.getText());
            if (b == false) {
                mostrarProductos(txtBuscar.getText());
            }
            sumarTotal();
            txtBuscar.setText("");
        }
     
        
        
    }//GEN-LAST:event_txtBuscarKeyPressed

    private void tblRecibosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblRecibosKeyPressed
        // TODO add your handling code here:
        DecimalFormat ft = new DecimalFormat("####.00");

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            for (int i = 0; i < tblRecibos.getRowCount(); i++) {
                int cantidad = Integer.parseInt(tblRecibos.getValueAt(i, 2).toString());
                double preciounitario = Double.parseDouble(tblRecibos.getValueAt(i, 3).toString());

                double totalparcial = cantidad * preciounitario;
                tblRecibos.setValueAt(ft.format(totalparcial), i, 6);
            }
            sumarTotal();
        }
        
    }//GEN-LAST:event_tblRecibosKeyPressed

